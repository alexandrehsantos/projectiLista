package com.example.demo;

public class ParametrosRetorno {
	String nomeCampoRetorno;
	String descricaoCampoRetorno;
	String tipoDadoRetorno;

	
	public String getNomeCampoRetorno() {
		return nomeCampoRetorno;
	}
	public String getDescricaoCampoRetorno() {
		return descricaoCampoRetorno;
	}
	public String getTipoDadoRetorno() {
		return tipoDadoRetorno;
	}
	public void setNomeCampoRetorno(String nomeCampoRetorno) {
		this.nomeCampoRetorno = nomeCampoRetorno;
	}
	public void setDescricaoCampoRetorno(String descricaoCampoRetorno) {
		this.descricaoCampoRetorno = descricaoCampoRetorno;
	}
	public void setTipoDadoRetorno(String tipoDadoRetorno) {
		this.tipoDadoRetorno = tipoDadoRetorno;
	}

}
